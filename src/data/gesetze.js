export function findePassendeGesetze(thema) {
  if (!thema) return [];
  const themaLower = thema.toLowerCase();
  const gefunden = new Set();
  Object.entries(THEMA_GESETZE).forEach(([schluessel, gesetze]) => {
    if (themaLower.includes(schluessel)) {
      gesetze.forEach(g => gefunden.add(g));
    }
  });
  // Immer § 1 StVO und § 1 FschAusbO als Basis
  gefunden.add('§ 1 StVO');
  gefunden.add('§ 1 FschAusbO');
  return GESETZE_LINKS.filter(g => gefunden.has(g.kuerzel));
}
